        <p>
         <b class="lt63">
          Giornalista: In cosa hanno fallito i metodi esistenti per la cura delle articolazioni?
                  Oggi nelle farmacie ci sono molti preparati differenti, no?
         </b>
        </p>
        <p class="lt64">
         <b>
          Thomas Blank:
         </b>
         Sì, in effetti esistono molti preparati per le articolazioni, le
                farmacie ne sono letteralmente piene. Tuttavia l'impressione che ci siano molti preparati per la
                rigenerazione dei tessuti è falsa. In realtà, tutti questi preparati agiscono in maniera identica. Chi
                abbia già avuto a che fare con patologie articolari sa bene che rigenerare completamente le
                articolazioni è praticamente impossibile.
        </p>
        <p class="lt65">
         Il 98% delle malattie articolari ha infatti luogo a causa delle alterazioni delle
                caratteristiche del tessuto cartilagineo stesso. Sotto l'influenza di diversi fattori sfavorevoli, il
                tessuto cartilagineo comincia a seccarsi. Le sue caratteristiche ammortizzanti diminuiscono, la
                cartilagine comincia a creparsi. Se lo strato cartilagineo diminuisce a causa dell'usura naturale,
                abbiamo l'artrosi. Se nelle fessura della cartilagine finisce un'infezione e l'articolazione si
                infiamma, abbiamo l'artrite. Se nell'articolazione le cellule cominciano a dividersi in maniera anomala,
                abbiamo un'artrite reumatoide. Con questo voglio dimostrare ancora che tutte le malattie articolari
                hanno una sola origine.
        </p>
        <p class="lt66">
         Ad oggi, i mezzi più efficaci per eliminare i dolori articolari sono i condroprotettori e
                le iniezioni di acido ialuronico. Questi mezzi aiutano effettivamente a rigenerare le articolazioni, ma
                solo temporaneamente, come dimostra la pratica. Essi, infatti, non eliminano la causa ultima del
                disfacimento articolare, per cui nell'arco di 3-6 mesi il problema si presenta di nuovo. Bisogna fare di
                nuovo le iniezioni ed assumere i condroprotettori.
        </p>
        <p>
         <b class="lt67">
