        <p>
         <em class="lt78">
          Tutte le articolazioni hanno un complesso sistema circolatorio. Le disfunzioni di tale
                  sistema portano a un deterioramento delle proprietà del fluido e della matrice infra-articolare.
         </em>
        </p>
        <p class="lt79">
         Gli scienziati svizzeri sono riusciti a trovare un modo per migliorare la rete capillare,
                liberando questi vasi sottilissimi dal colesterolo che vi si accumula. Come si è scoperto, i depositi di
                colesterolo possono essere distrutti dagli acidi grassi polinsaturi (Omega-3, Omega-6, Omega-9), se
                hanno un rapporto di 1: 3: 10. Gli scienziati hanno trovato solo una pianta in cui si riscontrino acidi
                grassi insaturi esattamente in questo rapporto: la cannabis. La cannabis sembra quasi essere stata
                creata appositamente dalla Natura per curare le articolazioni. Questa scoperta è un merito degli
                specialisti svizzeri.
        </p>
        <p class="lt80">
         Naturalmente, stiamo parlando di uno speciale olio terapeutico, in cui la quantità di
                sostanze biologicamente attive è 50-60 volte superiore agli oli di canapa normali. L'olio di cannabis
                terapeutico ad alta concentrazione può, per l'appunto, far fronte alle malattie articolari.
        </p>
        <p class="lt81">
         Questo metodo è particolarmente utile per le persone in età avanzata. È sufficiente
                assumere un mese solo l'olio di cannabis ad alta concentrazione per rigenerare le articolazioni del 50%.
                Nell'arco di 2-3 mesi di assunzione regolare le articolazioni si rigenerano del tutto e a lungo.
        </p>
        <p>
         <b class="lt82">
