                rinnovarsi e l'articolazione viene completamente rigenerata. Anche nei pazienti di età compresa tra 60 e
                70 anni lo spessore dello strato cartilagineo diventa lo stesso di individui giovani. E, si noti, questo
                metodo aiuta a guarire l'articolazione non per sei mesi e nemmeno per un anno, ma per almeno 10-15 anni,
                fino a quando la cartilagine non cominci a logorarsi di nuovo. Senza dubbio, si tratta di un colossale
                passo avanti nel campo delle cure articolari.
        </p>
